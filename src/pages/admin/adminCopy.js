export function formatAdminCopy(template, values = {}) {
  return String(template || '').replace(/\{(\w+)\}/g, (_, key) => values[key] ?? '')
}

export function getAdminRuntimeKind(runtimeConfig) {
  const label = String(runtimeConfig?.stagingLabel || '').toLowerCase()
  const apiBase = String(runtimeConfig?.apiBaseUrl || '').toLowerCase()
  if (runtimeConfig?.dataMode === 'mock') return 'development'
  if (label.includes('staging') || apiBase.includes('staging')) return 'staging'
  if (runtimeConfig?.isProd || label.includes('production') || apiBase === '/api' || apiBase.includes('noblesse.web.app/api')) return 'production'
  return 'development'
}

