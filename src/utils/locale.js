export const toTaiwanTraditional = (value) => {
  if (typeof value === 'string') {
    const phraseConverted = taiwanPhraseReplacements.reduce(
      (text, [from, to]) => text.split(from).join(to),
      value,
    )
    return phraseConverted.replace(/[\u3400-\u9fff]/g, (char) => simplifiedToTraditionalCharacters[char] || char)
  }
  if (Array.isArray(value)) return value.map(toTaiwanTraditional)
  if (typeof value === 'function') return (...args) => toTaiwanTraditional(value(...args))
  if (value && typeof value === 'object') {
    return Object.fromEntries(Object.entries(value).map(([key, entry]) => [key, toTaiwanTraditional(entry)]))
  }
  return value
}

export const resolveLocaleCopy = (dictionary, locale, fallback = 'kr') => {
  const canonicalLocale = canonicalizeLocale(locale)
  const contentLocale = getLocaleContentKey(canonicalLocale)
  const copy = dictionary?.[contentLocale] ?? dictionary?.[canonicalLocale] ?? dictionary?.[fallback] ?? dictionary?.en
  return canonicalLocale === taiwanLocale ? toTaiwanTraditional(copy) : copy
}

export const localizeText = (value, locale) => (
  canonicalizeLocale(locale) === taiwanLocale ? toTaiwanTraditional(value) : value
)

